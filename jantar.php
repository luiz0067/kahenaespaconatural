                  <b>SOBRE OS PRATOS</b><br>

No jantar será servido o Bife com muita salada, mandioca, arroz, lasanha, carne de boi, de frango.
IMPORTANTE: Tera opção vegetariana para aqueles que não comem carne.
o prato principal Carne de Bufalo que será servido no espeto corrido, pois é mais saborosa quente.
Propriedade da carne de Bufalo:<br><br>
• O Búfalo e considerado por muitas tribos indígenas com sendo um símbolo de abundancia, pois a carne alimentava o povo,
 o couro fornecia vestuário e abrigo, os osso e tendões que forneciam ferramentas de sobrevivência.<br>
• Na Saúde, é a carne super 'light' do terceiro milênio. Com 40% menos de colesterol em relação ao bovino; 55% menos calorias,
 11% mais de proteínas e 10% a mais de minerais. Para cada R$ 1 que o governo aplica na divulgação das propriedades da carne do búfalo,
  economizará R$ 10 com remédios em doenças de circulação. <br>
• Curiosidade Após o acidente nuclear em Chernobyl, na antiga União Soviética, o leite de búfala foi o alimento que mais rapidamente 
eliminou os resíduos de radioatividade, a ponto de ser considerado alimento estratégico para o caso de catástrofes nucleares.
